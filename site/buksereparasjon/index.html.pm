◊h2{Fordeler med å reparere}

◊h3{Lokalt næringsliv}

Når du betaler en skredder i din egen by for å reparere buksa bidrar du til den
lokale økonomien og ikke til multinasjonale motekonserner. Samtidig er du med på
å opprettholde et marked for reparasjonstjenester.

◊h3{Forsterkning}

Avhengig av hvordan tøyet er reparert kan det være at det har blitt mer
slitesterkt, og det på et sted du allerede vet det kan oppstå slitasje

◊h3{Pris}

Jeg fikk hver bukse reparert for 350 kroner. Det er i samme størrelsesorden som
en billig bukse, men billigere enn en dyr bukse.

◊h3{Energi og miljø}

Klesproduksjon bruker mye energi. Reparasjon bruker mindre. Lavere energiforbruk
betyr som regel mindre skade på miljøet.

◊h2{Ulemper med å reparere}

Hvis du i stedet for å reparere tøyet kjøper nytt, bidrar du til økonomisk
utvikling der tøyet produseres. Ofte er det snakk om fremvoksende økonomier hvor
mindre beløp kan gjøre en større forskjell enn her hjemme.

For at det skal være noe poeng i å betale for en reparasjon må plagget holde en
viss kvalitet. Både for at det ikke skal bli ødelagt igjen med det samme og for
at det skal være et økonomisk valg. Hvis plagget kostet 300 kroner nytt og
koster 350 kroner å reparere, må reparasjonen øke kvaliteten. For eksempel ved
at det reparerte plagget er forsterket i kritiske områder og dermed mer holdbart
etter reparasjon en et tilsvarende nytt plagg.

Alternativt kan reparasjonen tilføre annen type verdi. Stolthet over å ha
trosset bruk-og-kast-samfunnet, eller at klær med synlig spor av reparasjon er
en statusmarkør. Det kan gi status i miljøer som er opptatt av bærekraft, og det
kan signalisere at man kjøper kvalitetsplagg som er verdt å reparere.
