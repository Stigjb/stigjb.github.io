Klær har en lei tendens til å bli slitt i bruk. Før eller siden finner man
rifter eller hull som man ikke kan ignorere - eller, hvis man gir blaffen, som
omverdenen ikke vil ignorere for deg.

Selv har jeg to bukser det var slitt hull på. Begge er dongeribukser
◊sn["sn-denim"]{også kjent som olabukser, denim eller ◊fl["en"]{jeans}} og har
blitt slitt på samme sted, men de har forskjellig stoffkvalitet.

